public class patternProgram1 {

	public static void main(String[] args) {
		// print a star triangle single side
//		*
//		**
//		***
//		****
//		*****
		for (int i=1; i<=5; i++) { // start for 1st for loop (row)
				for (int j=1; j<=i; j++) {// start for 2nd for loop (colum)
					System.out.print("*");
	} // end of 2nd for loop 
System.out.println();
	}}}// end of 1st for loop
